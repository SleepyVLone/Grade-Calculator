using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace StudentGradeCalculator
{
    public class LowerBandStudent
    {
        string lowerB;
        string lowerC;
        string lowerD;
        string lowerE;
        string lowerF;
        string lowerU;

        public LowerBandStudent(string B, string C, string D, string E, string F, string U)
        {
            lowerB = B;
            lowerC = C;
            lowerD = D;
            lowerE = E;
            lowerF = F;
            lowerU = U;
        }
    }
}
